    private void calculate() {
        switch (operation) {
            case '/': {
                answer = number / Double.parseDouble(txtArea.getText());
                txtArea.setText(Double.toString(answer));
                break;
            }
            case '+': {
                answer = number + Double.parseDouble(txtArea.getText());
                txtArea.setText(Double.toString(answer));
                break;
            }
            case '-': {
                answer = number - Double.parseDouble(txtArea.getText());
                txtArea.setText(Double.toString(answer));
                break;
            }
            case '*': {
                answer = number * Double.parseDouble(txtArea.getText());
                txtArea.setText(Double.toString(answer));
                break;
            }
            case '%': {
                answer = number % Double.parseDouble(txtArea.getText());
                txtArea.setText(Double.toString(answer));
            }
            default: {
                answer = 0;
            }
        }
    }

